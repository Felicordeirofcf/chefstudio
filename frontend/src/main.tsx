import React from 'react';
import ReactDOM from 'react-dom/client';
import { createBrowserRouter, RouterProvider, Navigate, useLocation } from 'react-router-dom';
import App from './App';
import Login from './components/auth/Login';
import Register from './components/auth/Register';
import DashboardLayout from './components/layout/DashboardLayout';
import DashboardHome from './components/dashboard/Dashboard';
import ProfilePage from './components/dashboard/ProfilePage';
import PlansPage from './components/dashboard/PlansPage';
import ConnectMeta from './components/auth/ConnectMeta';
import MetaCallback from './components/auth/MetaCallback';
import AnunciosTabsContainer from './components/AnunciosTabsContainer';
import { MetaAdsProvider } from './contexts/MetaAdsContext'; // <<< IMPORTAR O PROVIDER
import './index.css';

// üîê L√™ informa√ß√µes do usu√°rio armazenadas localmente
const getUserInfo = () => {
  const userInfo = localStorage.getItem('userInfo');
  if (!userInfo) return null;
  try {
    return JSON.parse(userInfo);
  } catch (e) {
    localStorage.removeItem('userInfo');
    return null;
  }
};

// üîí Rota protegida simplificada - apenas verifica login b√°sico
const ProtectedRoute = ({ children }: { children: React.ReactNode }) => {
  const userInfo = getUserInfo();
  const location = useLocation();
  
  // Verificar apenas autentica√ß√£o b√°sica
  if (!userInfo || !userInfo.token) {
    console.log("ProtectedRoute: Usu√°rio n√£o autenticado, redirecionando para login");
    return <Navigate to="/" state={{ from: location }} replace />;
  }

  // Permitir acesso a todas as rotas protegidas se o usu√°rio estiver autenticado
  return children;
};

// üåê Define as rotas principais
const router = createBrowserRouter([
  {
    path: "/",
    element: <App />, // App como elemento raiz
    children: [
      {
        index: true,
        element: <Login />,
      },
      {
        path: "/register",
        element: <Register />,
      },
      {
        path: "/connect-meta", // Esta rota pode precisar do contexto tamb√©m?
        element: (
          <ProtectedRoute>
            <ConnectMeta />
          </ProtectedRoute>
        ),
      },
      {
        path: "/meta-callback", // Esta rota pode precisar do contexto tamb√©m?
        element: (
          <ProtectedRoute>
            <MetaCallback />
          </ProtectedRoute>
        ),
      },
      {
        path: "/dashboard",
        element: (
          <ProtectedRoute>
            <DashboardLayout />
          </ProtectedRoute>
        ),
        children: [
          {
            index: true,
            element: <DashboardHome />,
          },
          {
            path: "profile",
            element: <ProfilePage />,
          },
          {
            path: "plans",
            element: <PlansPage />,
          },
          {
            path: "anuncios", // Esta rota provavelmente n√£o √© necess√°ria aqui, pois AnunciosTabsContainer √© renderizado dentro do DashboardHome
            element: <AnunciosTabsContainer />,
          },
        ],
      },
    ],
  },
]);

// Renderiza√ß√£o do React
ReactDOM.createRoot(document.getElementById('root')!).render(
  <React.StrictMode>
    {/* Envolver o RouterProvider com o MetaAdsProvider */}
    <MetaAdsProvider>
      <RouterProvider router={router} />
    </MetaAdsProvider>
  </React.StrictMode>
);

